    // Para actualizar la informacion y cada valor de la CPU en la interfaz
    public void updateCPUDisplay() {
        //Actualizar el numero de CPU (1,2,3)
        CPUIDVAR.setText(String.valueOf(cpu.getCpuId()));
        
        // Actualizer los valores del proceso en el CPU display
        
        ProcesoVAR.setText(cpu.getCurrentProcess() != null ? cpu.getCurrentProcess().getName() : "Proceso");
        IDVAR.setText(cpu.getCurrentProcess() != null ? String.valueOf(cpu.getCurrentProcess().getProcessID()) : "No Disponible" );
        EstadoVAR.setText(cpu.getCurrentProcess() != null ? String.valueOf(cpu.getCurrentProcess().getState()) : "No Disponible");
        PCEstadoVAR.setText(cpu.getCurrentProcess() != null ? String.valueOf(cpu.getCurrentProcess().getProgramCounter()) : "No Disponible");
        EstadoMARVAR.setText(cpu.getCurrentProcess() != null ? String.valueOf(cpu.getCurrentProcess().getMemoryAddressRegister()) : "No Disponible");
    }
    
    
    
    
    
